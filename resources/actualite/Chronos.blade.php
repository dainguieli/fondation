             <h2 class="text-primary">La Fondation Glory Impact Group a eu une fructueuse séance de travail avec M. Rolan LLAMAS Directeur Général Adjoint de Chronos Athletics. Koutouan Djoro Leonard .</h2>
             <hr >
             <p style="text-align: justify">La Fondation Glory Impact Group a eu une fructueuse séance de travail avec M. Rolan LLAMAS Directeur Général Adjoint de Chronos Athletics, équipementier sportif basé à Manille en Philippines et spécialisé dans la confection de tenues de sport (football, basketball, volleyball, handball, tennis, survêtements, polos etc...).

              Depuis 2022, Glory Impact Group est le représentant exclusif du Group Chronos Athletics en Afrique.<br><br> Ce partenariat stratégique permet à la Fondation Glory Impact Group de fournir des tenues de sport de qualité supérieure aux équipes et clubs sportifs sur le continent africain.
              
              L'objectif final de ce partenariat est d'aboutir à l'installation des ateliers de confection de tenues de sports de qualité supérieure en Côte d'Ivoire. Cela permettra non seulement de favoriser la formation et l'emploi des jeunes, mais surtout de promouvoir le métier du textile qui est un grand secteur pourvoyeur d'emploi.
              
              <br><br> Les ateliers de Chronos Athletics produisent des tenues de sport de haute qualité pour les athlètes professionnels et amateurs. Les tenues sont fabriquées avec des matériaux de qualité supérieure pour garantir un confort optimal et une meilleure performance.<br><br>
              
              Grâce à ce partenariat avec Chronos Athletics, la Fondation Glory Impact Group peut offrir à des équipes et clubs sportifs des tenues de sport sur mesure qui répondent à leurs besoins spécifiques en termes de conception, de fonctionnalité et de durabilité.
              
              <br><br>  En résumé, ce partenariat avec Chronos Athletics permet à la Fondation Glory Impact Group de fournir des tenues de sport de qualité supérieure en Afrique, de favoriser la formation et l'emploi des jeunes, de promouvoir le métier du textile et de soutenir les équipes et clubs sportifs dans leur quête de performances optimales.</p>
