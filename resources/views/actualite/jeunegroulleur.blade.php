             <h2 class="text-primary">Déjeuner de Noel avec les jeunes battants et grouilleurs qui a eu lieu à Cocody II Plateaux.</h2>
             <hr >
             <p style="text-align: justify">Le dimanche 25 décembre 2022, La Fondation Glory Impact Group a organisé "Le Déjeuner de Noël avec des jeunes battants et grouilleurs". 
              Il se décline donc en une cérémonie ouverte aux jeunes sans emploi formel. Ce déjeuner nous l'offrons surtout, et cela à partir de cette année, aux jeunes vendeurs ambulants, les "djosseurs de namman" les "aide-maçons" , les vendeuses de fruits, les coiffeuses, les couturières, les restauratrices etc... issus de familles défavorisées ou démunies. Il est donc destiné à ces jeunes gens conscients de la précarité sociale dans laquelle ils vivent mais qui se débrouillent tant bien que mal pour en sortir. <br><br>
              Le president de la Fondation  M Jules Ekanza Gouhan, accompagné de son épouse Becely Kecys Gouhan, vice-présidente de la Fondation Glory Impact Group, ont partagé un repas avec une centaine de jeunes. Ils sont venus de différentes communes du District d’Abidjan.  <br><br>
              Au cours de cette agréable cérémonie, ils ont eu à les sensibiliser sur la nécessité pour eux d'avoir une idée novatrice à mettre en oeuvre : UN PROJET. Il est pour eux une voie qui leur permettra de sortir de la précarité, car forts de leurs bonnes dispositions d'esprit, ils sont capables de créer des entreprises, de vraies. Ils ont notamment eu à leur révéler leurs débuts, de lycéen et entrepreneur qui ont vendu du lait "caillé", du savon liquide, et bien d'autres expériences. 
              Sans langue de bois, ils les ont exhorté à développer un mental fort et à vivre tels des "orphelins" qui n'ont que Dieu pour se réaliser. <br><br>
              Nombreux sont ceux parmi eux qui nous ont exprimé leur désire de bien formaliser leurs activités mais qu'ils ne savent pas comment y arriver. <br><br>
              La Fondation Glory Impact Group est la bonne adresse pour l'écoute, le suivi, l'assistance des jeunes désireux d'entreprendre, nous les avons ainsi rassurés. 
              Cette petite fête que nous avons énormément appréciée pour sa sobriété, son sens et la mobilisation des jeunes est une institution: Rdv est pris pour 2023... Elle s'est terminée dans la joie. Nous avons posé ensemble pour une photo de famille. "Le Déjeuner de Noël des jeunes battants et grouilleurs" s'est tenu sur le terrain de basket des 2 plateaux, non loin de Sococé, à Cocody.<br><br>
              Fondation Glory Impact Group, Ensemble pour une jeunesse plus forte !!!<br><br>
              Bonne et Glorieuse année 2023 à toutes et à tous !!!</p>
        
